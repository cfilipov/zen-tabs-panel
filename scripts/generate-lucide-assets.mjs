import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceIconsDir = path.join(repoRoot, "node_modules", "lucide-static", "icons");
const tagsPath = path.join(repoRoot, "node_modules", "lucide-static", "tags.json");
const generatedDir = path.join(repoRoot, "src", "popup", "generated");
const generatedMetadataPath = path.join(generatedDir, "lucide-icons.ts");
const distIconsDir = path.join(repoRoot, "dist", "lucide-icons");

function humanize(name) {
  return name
    .split("-")
    .map((part) => part ? part[0].toUpperCase() + part.slice(1) : part)
    .join(" ");
}

function themeSvg(svg) {
  return svg
    .replace(/<!--[\s\S]*?-->\s*/g, "")
    .replace(/\sclass="[^"]*"/, "")
    .replace(/\swidth="24"/, "")
    .replace(/\sheight="24"/, "")
    .replace(/stroke="currentColor"/g, 'stroke="context-fill"')
    .replace(/>\s+</g, "><")
    .trim();
}

const files = (await readdir(sourceIconsDir))
  .filter((file) => file.endsWith(".svg"))
  .sort((a, b) => a.localeCompare(b));

const tags = JSON.parse(await readFile(tagsPath, "utf8"));
const icons = [];

await mkdir(generatedDir, { recursive: true });
await rm(distIconsDir, { recursive: true, force: true });
await mkdir(distIconsDir, { recursive: true });

for (const file of files) {
  const name = file.slice(0, -".svg".length);
  const svg = themeSvg(await readFile(path.join(sourceIconsDir, file), "utf8"));
  await writeFile(path.join(distIconsDir, file), svg + "\n");
  icons.push({
    name,
    label: humanize(name),
    terms: Array.isArray(tags[name]) ? tags[name] : [],
  });
}

const metadataSource = `// Generated by scripts/generate-lucide-assets.mjs from lucide-static.
// Do not edit by hand.

export type LucideIconMeta = {
  name: string;
  label: string;
  terms: readonly string[];
};

export const LUCIDE_ICONS = ${JSON.stringify(icons)} as const satisfies readonly LucideIconMeta[];
`;

await writeFile(generatedMetadataPath, metadataSource);
console.log(`Generated ${icons.length} Lucide icons`);
